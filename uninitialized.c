// Declares some floats and ints without initializing them, prints them.

#include <stdio.h>

int main(void)
{
    int first, second, third;
    float firstf, secondf, thirdf;

    printf("%d\n", first);
    printf("%d\n", second);
    printf("%d\n", third);
    printf("%f\n", firstf);
    printf("%f\n", secondf);
    printf("%f\n", thirdf);

    return 0;
}